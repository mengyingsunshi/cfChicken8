package com.eoulu.entity;

public class GooseMy {
	
	private int ID;
	private String Name;
	private String Department;
	private int PossessionNum;
	private int TradableNum;
	private String OperatingTime;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getDepartment() {
		return Department;
	}
	public void setDepartment(String department) {
		Department = department;
	}
	public int getPossessionNum() {
		return PossessionNum;
	}
	public void setPossessionNum(int possessionNum) {
		PossessionNum = possessionNum;
	}
	public int getTradableNum() {
		return TradableNum;
	}
	public void setTradableNum(int tradableNum) {
		TradableNum = tradableNum;
	}
	public String getOperatingTime() {
		return OperatingTime;
	}
	public void setOperatingTime(String operatingTime) {
		OperatingTime = operatingTime;
	}
	
	

}
